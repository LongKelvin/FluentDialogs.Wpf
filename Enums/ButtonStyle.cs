namespace FluentDialogs.Enums;

/// <summary>
/// Specifies the visual style of a button in a message box dialog.
/// </summary>
public enum ButtonStyle
{
    /// <summary>
    /// Default button style with standard appearance.
    /// </summary>
    Default,

    /// <summary>
    /// Primary button style with accent color highlighting.
    /// </summary>
    Primary,

    /// <summary>
    /// Secondary button style with subtle appearance.
    /// </summary>
    Secondary,

    /// <summary>
    /// Danger button style indicating a destructive or critical action.
    /// </summary>
    Danger
}
