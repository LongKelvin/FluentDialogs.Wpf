namespace FluentDialogs.Enums;

/// <summary>
/// Specifies the icon displayed in a message box dialog.
/// </summary>
public enum MessageBoxIcon
{
    /// <summary>
    /// No icon is displayed.
    /// </summary>
    None,

    /// <summary>
    /// An information icon is displayed.
    /// </summary>
    Info,

    /// <summary>
    /// A warning icon is displayed.
    /// </summary>
    Warning,

    /// <summary>
    /// An error icon is displayed.
    /// </summary>
    Error,

    /// <summary>
    /// A question icon is displayed.
    /// </summary>
    Question,

    /// <summary>
    /// A success icon is displayed.
    /// </summary>
    Success
}
